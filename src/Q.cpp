void Qs::mostrarQs() const{
    for (int i = 0; i < QsVector.size(); i++) {
        std::cout << QsVector.at(i) << ",";
    }
}
void Qs::imprimirQs(std::fstream &file) const {
    for (int i = 0; i < stateMatrix::tamanoVectorIO; i++) {
        if (i < QsVector.size()) {
            file << QsVector.at(i) << ',';
        } else {
            file << "0,";
        }
    }  
}

